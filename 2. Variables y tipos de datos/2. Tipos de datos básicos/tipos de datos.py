# Cadenas de caracteres -> class str
mensaje = 'Esto es un texto de prueba'
print(type(mensaje))
letra = 'a'
print(type(letra))
numero_5 = '5'
print(type(numero_5))

# NumÃ©ricos
# - Enteros  -> class int
numero = -5
print(type(numero))


# - Flotantes -> class float
n_decimal = 2.4
print(type(n_decimal))

suma_decimal = 2.1 + 3.2
print(type(suma_decimal))

calculo = 1/5
print(type(calculo))



# - Complejos -> class complex
complejo = 1 + 2j
print(type(complejo.imag))

# Booleanos -> class bool

curso_aprobado = True
nivel_dificil = False

print(type(nivel_dificil))


suma = 4 + 5 == 9
print(suma)